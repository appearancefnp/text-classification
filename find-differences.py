tfhub_handle_encoder = map_name_to_handle[bert_model_name]
tfhub_handle_preprocess = map_model_to_preprocess[bert_model_name]

def build_classifier_model():
    text_input = tf.keras.layers.Input(shape=(), dtype=tf.string, name='text')
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)
    encoder = hub.KerasLayer(tfhub_handle_encoder, trainable=True, name='BERT_encoder')
    outputs = encoder(encoder_inputs)
    net = outputs['pooled_output']
    net = tf.keras.layers.Dropout(0.1)(net)
    net = tf.keras.layers.Dense(5, activation='softmax', name='sentiment')(net)
    return tf.keras.Model(text_input, net)

def process_text(teksts):
    # lowercase
    teksts = teksts.lower()

    # remove punctuation
    teksts = teksts.translate(str.maketrans("","", string.punctuation))

    # remove stop words
    jauns_teksts = []
    for word in teksts.split(" "):
        if word in stopword:
            continue
        jauns_teksts.append(word)
    
    teksts = " ".join(jauns_teksts)


    return teksts



def main(argv):

    # load the BERT model
    optimizer = optimization.create_optimizer(init_lr=0.001,
                                          num_train_steps=1,
                                          num_warmup_steps=1,
                                          optimizer_type='adamw')

    bert_model = tf.keras.models.load_model("sentiment-model", custom_objects={"AdamWeightDecay": optimizer})

    # load the Naive Bayes model
    naive_model = pickle.load(open("naive-classifier.pck", 'rb'))


    train_data, validation_data, test_data = tfds.load(
        name="sentiment140",
        split=('train[:80%]', 'train[80%:]', 'test'),
        as_supervised=True)

    # get validation corpus
    for pair in validation_data.take(100):
        teksts, sentiments = pair
        teksts = teksts.numpy().decode("UTF-8")
        sentiments = "Negative" if sentiments.numpy() == 0 else "Positive"

        processed_text = process_text(teksts)

        # predict bert
        prediction = bert_model.predict([teksts])[0]
        bert_predicted_class = "Negative" if np.argmax(prediction) == 0 else "Positive"

        # predict naive
        prediction = naive_model.predict([processed_text])[0]
        naive_predicted_class = "Negative" if prediction == 0 else "Positive"

        # check if different
        if naive_predicted_class != bert_predicted_class:
            print(f"{teksts}")
            print(f"BERT:{bert_predicted_class}")
            print(f"NAIVE:{naive_predicted_class}")
            print(f"REAL: {sentiments}")
            print()
if __name__ == '__main__':
    app.run(main)
