tfhub_handle_preprocess = map_model_to_preprocess[bert_model_name]

def build_classifier_model():
    text_input = tf.keras.layers.Input(shape=(), dtype=tf.string, name='text')
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)
    encoder = hub.KerasLayer(tfhub_handle_encoder, trainable=True, name='BERT_encoder')
    outputs = encoder(encoder_inputs)
    net = outputs['pooled_output']
    net = tf.keras.layers.Dropout(0.1)(net)
    net = tf.keras.layers.Dense(5, activation='softmax', name='sentiment')(net)
    return tf.keras.Model(text_input, net)

def process_text(teksts):
    # lowercase
    teksts = teksts.lower()

    # remove punctuation
    teksts = teksts.translate(str.maketrans("","", string.punctuation))

    # remove stop words
    jauns_teksts = []
    for word in teksts.split(" "):
        if word in stopword:
            continue
        jauns_teksts.append(word)
    
    teksts = " ".join(jauns_teksts)


    return teksts



