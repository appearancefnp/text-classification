train_data = train_data.map(lambda x, y: (x, tf.one_hot(y, number_of_classes)))
validation_data = validation_data.map(lambda x, y: (x, tf.one_hot(y, number_of_classes)))

tfhub_handle_encoder = map_name_to_handle[bert_model_name]
tfhub_handle_preprocess = map_model_to_preprocess[bert_model_name]

# load pre-processing layer
bert_preprocess_model = hub.KerasLayer(tfhub_handle_preprocess)

def build_classifier_model():
    text_input = tf.keras.layers.Input(shape=(), dtype=tf.string, name='text')
    preprocessing_layer = hub.KerasLayer(tfhub_handle_preprocess, name='preprocessing')
    encoder_inputs = preprocessing_layer(text_input)
    encoder = hub.KerasLayer(tfhub_handle_encoder, trainable=True, name='BERT_encoder')
    outputs = encoder(encoder_inputs)
    net = outputs['pooled_output']
    net = tf.keras.layers.Dropout(0.1)(net)
    net = tf.keras.layers.Dense(number_of_classes, activation='softmax', name='sentiment')(net)
    return tf.keras.Model(text_input, net)
# create bert classifier model
classifier_model = build_classifier_model()

# metrics & loss
loss = tf.keras.losses.CategoricalCrossentropy()
metrics = tf.metrics.CategoricalAccuracy()

# optimizer
epochs = 3
steps_per_epoch = tf.data.experimental.cardinality(train_data).numpy()
num_train_steps = steps_per_epoch * epochs
num_warmup_steps = int(0.1*num_train_steps)

init_lr = 3e-5
optimizer = optimization.create_optimizer(init_lr=init_lr,
                                          num_train_steps=num_train_steps,
                                          num_warmup_steps=num_warmup_steps,
                                          optimizer_type='adamw')

# compile
classifier_model.compile(optimizer=optimizer,
                         loss=loss,
                         metrics=metrics)

tensorboard = tf.keras.callbacks.TensorBoard(log_dir='logs-140')
checkpoint = tf.keras.callbacks.ModelCheckpoint("sentiment-model", monitor='val_loss', verbose=0, save_best_only=True, save_weights_only=False, mode='auto', save_freq='epoch', options=None)


print(f'Training model with {tfhub_handle_encoder}')
history = classifier_model.fit(x=train_data.shuffle(10000).batch(64),
                               validation_data=validation_data.batch(64),
                               epochs=epochs,
                               callbacks=[tensorboard, checkpoint])

classifier_model.save("sentiment-model")
